def desimal_ke_biner(angka):
    biner = ""
    while angka > 0:
        sisa = angka % 2
        biner = str(sisa) + biner
        angka = angka // 2
    return biner

def biner_ke_desimal(biner):
    desimal = 0
    panjang = len(biner)
    for i in range(panjang):
        digit = int(biner[i])
        desimal += digit * (2 ** (panjang - i - 1))
    return desimal

