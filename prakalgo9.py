def konversi_list_ke_tuple():
    input_list = input("Masukkan elemen-elemen list (pisahkan dengan koma): ").split(',')

    input_list = [int(elem.strip()) for elem in input_list]

    output_tuple = tuple(input_list)
    return output_tuple

tuple_output = konversi_list_ke_tuple()
print("Tuple Output:", tuple_output)

def hitung_rata_rata(tuple_input):
    total = 0
    jumlah_elemen = 0

    for tup in tuple_input:
        for elemen in tup:
            total += elemen
            jumlah_elemen += 1

    if jumlah_elemen == 0:
        return None  # Menghindari pembagian oleh nol
    else:
        rata_rata = total / jumlah_elemen
        return rata_rata

# Contoh penggunaan
tuple_contoh = ((31,32,33),(34,35,36),(37,38,39))
rata_rata_hasil = hitung_rata_rata(tuple_contoh)

if rata_rata_hasil is not None:
    print("Tuple:", tuple_contoh)
    print("Rata-rata:", rata_rata_hasil)
else:
    print("Tidak ada elemen untuk dihitung rata-ratanya.")

