def hitung_hasil_kali(list_input):
    hasil_kali = 1

    for nilai in list_input:
        hasil_kali *= nilai

    return hasil_kali

# Contoh penggunaan
list_contoh = [2, 3, 4, 5]
hasil_kali = hitung_hasil_kali(list_contoh)

print("List:", list_contoh)
print("Hasil Kali:", hasil_kali)

def hitung_jumlah_string(list_input):
    jumlah_string = 0

    for string in list_input:
        if len(string) >= 2 and string[0] == string[-1]:
            jumlah_string += 1

    return jumlah_string

# Contoh penggunaan
list_contoh = ["aba", "xyz", "level", "radar", "python", "pop"]
jumlah_string_hasil = hitung_jumlah_string(list_contoh)

print("List:", list_contoh)
print("Jumlah String:", jumlah_string_hasil)